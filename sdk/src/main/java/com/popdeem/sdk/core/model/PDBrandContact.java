package com.popdeem.sdk.core.model;

/**
 * Popdeem Brand Contact Model Class
 */
public class PDBrandContact {

    private String phone;
    private String email;
    private String web;
    private String facebook;
    private String twitter;

    public PDBrandContact() {
    }

    public PDBrandContact(String phone, String email, String web, String facebook, String twitter) {
        this.phone = phone;
        this.email = email;
        this.web = web;
        this.facebook = facebook;
        this.twitter = twitter;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getWeb() {
        return web;
    }

    public void setWeb(String web) {
        this.web = web;
    }

    public String getFacebook() {
        return facebook;
    }

    public void setFacebook(String facebook) {
        this.facebook = facebook;
    }

    public String getTwitter() {
        return twitter;
    }

    public void setTwitter(String twitter) {
        this.twitter = twitter;
    }

}
